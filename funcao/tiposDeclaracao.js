//function declaration 
//essas funcoes sao carregadas primeiro antes de executar o codigo
function soma(x,y){
    return x+y
}

//function expression
//so pode ser chamada apos ser declarada
const sub = function (x,y){return x-y}

//named function expression
//so pode ser chamada apos ser declarada
const mult = function mult(x,y){
    return x*y
}

